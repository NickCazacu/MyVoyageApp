/**
 * Место назначения: страна или город.
 *
 * [name] — то, что подставляется в поле направления (город или страна).
 * [country] — страна для города (для подсказки и поиска); null, если это сама страна.
 */
data class Place(
    val name: String,
    val country: String? = null
) {
    /** Признак страны (а не города). */
    val isCountry: Boolean get() = country == null

    /** Текст для списка: "Город · Страна" либо просто название страны. */
    val label: String get() = if (country != null) "$name · $country" else name

    /** Совпадение с поисковым запросом по названию или стране. */
    fun matches(query: String): Boolean {
        val q = query.trim()
        if (q.isEmpty()) return true
        return name.contains(q, ignoreCase = true) ||
            (country?.contains(q, ignoreCase = true) == true)
    }
}

